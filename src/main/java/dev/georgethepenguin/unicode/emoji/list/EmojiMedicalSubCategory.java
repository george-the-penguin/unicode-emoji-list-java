package dev.georgethepenguin.unicode.emoji.list;

import static dev.georgethepenguin.unicode.emoji.list.utils.UnicodeUtils.toPrint;

/**
 * Unicode Emoji Sub-Category: Medical.
 *
 * @author Jorge Garcia (George the Penguin).
 * @version 1.0.0-v15.1
 * @since 17
 */
@SuppressWarnings("unused")
public enum EmojiMedicalSubCategory {

    /**
     * Syringe.
     */
    SYRINGE(EmojiCategory.OBJECTS,1370L,"U+1F489","syringe"),

    /**
     * Drop of blood.
     */
    DROP_OF_BLOOD(EmojiCategory.OBJECTS,1371L,"U+1FA78","drop of blood"),

    /**
     * Pill.
     */
    PILL(EmojiCategory.OBJECTS,1372L,"U+1F48A","pill"),

    /**
     * Adhesive bandage.
     */
    ADHESIVE_BANDAGE(EmojiCategory.OBJECTS,1373L,"U+1FA79","adhesive bandage"),

    /**
     * Crutch.
     */
    CRUTCH(EmojiCategory.OBJECTS,1374L,"U+1FA7C","crutch"),

    /**
     * Stethoscope.
     */
    STETHOSCOPE(EmojiCategory.OBJECTS,1375L,"U+1FA7A","stethoscope"),

    /**
     * X-ray.
     */
    X_RAY(EmojiCategory.OBJECTS,1376L,"U+1FA7B","x-ray");

    private final EmojiCategory emojiCategory;
    private final Long number;
    private final String unicode;
    private final String cldr;

    /**
     * Constructor.
     *
     * @param emojiCategory    The emoji category.
     * @param number           The number.
     * @param unicode          The unicode.
     * @param cldr             The CLDR.
     */
    EmojiMedicalSubCategory(EmojiCategory emojiCategory, Long number, String unicode, String cldr) {
        this.emojiCategory = emojiCategory;
        this.number = number;
        this.unicode = unicode;
        this.cldr = cldr;
    }

    /**
     * Gets the emoji category.
     *
     * @return The emoji category.
     */
    public EmojiCategory getEmojiCategory() {
        return emojiCategory;
    }

    /**
     * Gets the number.
     *
     * @return The number.
     */
    public Long getNumber() {
        return number;
    }

    /**
     * Gets the unicode.
     *
     * @return The unicode.
     */
    public String getUnicode() {
        return unicode;
    }

    /**
     * Gets the CLDR.
     *
     * @return The CLDR.
     */
    public String getCldr() {
        return cldr;
    }

    /**
     * Gets the unicode to print the emoji.
     *
     * @return The unicode to print the emoji.
     */
    public String print() {
        return toPrint(unicode);
    }
}
